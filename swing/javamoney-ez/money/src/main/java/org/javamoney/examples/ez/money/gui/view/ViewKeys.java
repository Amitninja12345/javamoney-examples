// ViewKeys

package org.javamoney.examples.ez.money.gui.view;

/**
 * This enumerated class provides keys for the views.
 */
public
enum
ViewKeys
{
  /**
   * The view for displaying the budgets.
   */
  BUDGETS,
  /**
   * The view for displaying the accounts overview.
   */
  HOME,
  /**
   * The view for displaying the account's transactions.
   */
  REGISTER,
  /**
   * The view for displaying the totals.
   */
  TOTALS;
}
