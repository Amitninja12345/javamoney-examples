// CategoryReportSortByKeys

package org.javamoney.examples.ez.money.report;

/**
 * This enumerated class provides keys for the fields in a category report that
 * can be sorted.
 */
public
enum
CategoryReportSortByKeys
{
  /**
   * The category's total.
   */
  AMOUNT,
  /**
   * The category's identifier.
   */
  CATEGORY,
  /**
   * The category's group.
   */
  GROUP,

  /**
   * The category's Percent.
   */
  PERCENT;
}
